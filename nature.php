                    <p>Пещера “Леденика” се намира на 16км от Враца.<br> Тя е един от бисерите на Врачански балкан<br>  с изключителните си подземни образувания и приказни<br> зали. В продължение на хилядолетия водните капки<br> като ненадминат скулптор изваяли тази необикновена<br> пещера, дълга около 300м с 10 зали. През 2014 г. <br>по европейски проект съвместно с община Враца около пещерата <br>е изграден развлекателен парк “Леденика”. В него<br> се предлагат различни забавления като 5 D кино,<br> въжена градина, катерене по алпийска стена, <br>туристически разходки, спортни игри и състезания,<br> и други. Името си Леденика носи от ледените сталагмити,<br> сталактити и сталактони, които се образуват през зимата <br>в началото близо до входа. В пещерата има малко,<br> немного дълбоко синтрово езеро, което според едно <br>поверие изпълнява желанието на всеки, който потопи<br> ръката си в него. Туристическата обиколка на<br> пещерата трае около час, а маршрутът обхваща 350 м<br> от пещерните галерии. 
                        
