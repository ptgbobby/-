                    <p>Скакля е най-високият непостоянно течащ водопад<br>
                        в България и на Балканският полуостров. С невероятните<br>
                        си 141 m, той е природно чудо.  Красив е през всички сезони,<br>
                        и от горната му част се откриват прекрасни гледки към Враца.<br>
                        Той също е  пресечна точка на няколко туристически маршрута.<br>
                        Водопадът е най-атрактивен през слънчевите пролетни дни, когато<br>
                        се топят снеговете и грохотът падащата вода спира дъха на всеки<br>
                        турист. За разлика от останалите планински водопади, водната каскада<br>
                        на Скакля може да се наблюдава и от центъра на гр. Враца. Към водопада<br>
                        води маркирана пътека, която се изминава за около 2 часа. 
                        
