                  <h1>Проходът <span>“Вратцата”</span></h1>
                  <p>Проходът “Вратцата”  наричан в средновековието <br>Вратица, е символ на град Враца и се свързва с <br>името на града. Той се простира върху площ от 2 хектара.<br> Отвесните скали на Вратцата са най-високите<br> на тази надморска височина на Балканския<br> полуостров — над 400 м. и са най-популярният алпийски <br>обект в България.По тях са трасирани над <br>140 алпийски маршрути с различна категория <br>на трудност, някои от които са емблематични за<br> българския алпинизъм. В пролома са <br>запазени останки от средновековната крепост<br> Вратица – X-XII век. Тук се е издигало старото<br> укрепление и крепостните съоръжения, а стръмните планински<br> склонове са били естествена  “порта” за пътя и<br> средновековното селище по протежение на река Лева. Предназначението <br>й е било да прегражда пътя на славянските отпреди <br> през Врачанския пролом към Сердика и югозападните части <br>на Балканския полуостров.Проходът Вратцата е<br> притегателно място  за алпинисти и туристи, поради <br>своята достъпност и впечатляващи гледки към скалните<br> отвеси. Гледката към скалите смразява дъха.
                      
