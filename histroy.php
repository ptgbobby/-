                          <h1>Площад <span>“Христо Ботев”</span></h1>
                  <p>Площад "Христо Ботев" е централният площад във Враца.<br> Името му идва от намиращият се на него величествен паметник<br> на революционера Христо Ботев. Тук, на първи юни с<br> тържествена церемония се откриват "Ботеви дни".<br> До площада, се намира Кулата на “Мешчиите”, Регионален<br> исторически музей, театъра, а малко под <br>площада - Читалище Развитие,и Община Враца.<br> На 27.05.1890 год. на площада е открит първият паметник на<br> Христо Ботев в България. Инициативата <br>за построяването му във Враца е на кмета на града <br>Цено Леонкев и председателя на Постоянната комисия<br> Гьошо Антонов.
Паметникът е открит тържествено<br> от княз Фердинанд. Сред гостите са <br>министър-председателят Стефан Стамболов, майката<br> на Ботев, съпругата му Венета и дъщеря му Иванка.<br>
През 1955 г. паметникът е демонтиран и на негово място е поставена метална плоча. 

